import UIKit

// what is var
// var is a keyword that is used to declare a variable in swift
// var is used to declare a variable that can be changed later

var greeting = "Hello, playground"

print(greeting)

// what is let
// let is a keyword that is used to declare a constant in swift
// let is used to declare a variable that cannot be changed later
let firstGreeting = "Hello, playground"


print(firstGreeting)
// difference between var and let
// var is used to declare a variable that can be changed later
// let is used to declare a variable that cannot be changed later


// swift is a type safe language


//Number can be Int , Double , Float, CGFloat $ more

let booleanValue: Bool = true
let integerValue: Int = 10

// use double for mathematical operations
let doubleValue: Double = 10.011111111

// use CGFloat for UI related operations
let cgFloatValue: CGFloat = 10.0111111


// why CGFloat is used in UI related operations
// CGFloat is used in UI related operations because it is a type alias of either Float or Double



